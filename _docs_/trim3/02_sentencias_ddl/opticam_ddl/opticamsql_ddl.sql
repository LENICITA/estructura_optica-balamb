-- ------------------------------------------------------------------------------------- --
-- 01. CREAR BBDDS. ------------------------------------------------------------------ --
--     CREATE DATABASE __; : ---------------------------------------------------------------- -- 
-- ------------------------------------------------------------------------------------- --
CREATE DATABASE opticamdb;
-- ------------------------------------------------------------------------------------- --
-- 02. Mostrar BBDDs. ------------------------------------------------------------------ --
--     SHOW DATABASES : ---------------------------------------------------------------- -- 
-- ------------------------------------------------------------------------------------- --
SHOW DATABASES;

-- ------------------------------------------------------------------------------------- --
-- 03. Usar BBDD. ---------------------------------------------------------------------- --
--     USE __ : ------------------------------------------------------------------------ --
-- ------------------------------------------------------------------------------------- --
USE opticamdb;

-- ------------------------------------------------------------------------------------- --
-- 04. Eliminar BBDD. ------------------------------------------------------------------ --
--     DROP DATABASE __ : -------------------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
DROP DATABASE opticamdb;

-- ------------------------------------------------------------------------------------- --
-- 05. Mostrar Tablas. ----------------------------------------------------------------- --
--     SHOW TABLES __ : ---------------------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
SHOW TABLES;

-- ------------------------------------------------------------------------------------- --
-- 06. Mostar Columnas. ---------------------------------------------------------------- --
--     SHOW COLUMNS FROM __ . DESCRIBE __ : -------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
SHOW COLUMNS FROM USUARIOS;
DESCRIBE USUARIOS;

-- ------------------------------------------------------------------------------------- --
-- 07. Agregar Columna. ---------------------------------------------------------------- --
--     ALTER TABLE __ ADD __ __ : ------------------------------------------------------ --
-- ------------------------------------------------------------------------------------- --
ALTER TABLE CATEGORIAS ADD otras DATE;

-- ------------------------------------------------------------------------------------- --
-- 08. Renombrar Columna. -------------------------------------------------------------- --
--     ALTER TABLE __ CHANGE __ __ : --------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
ALTER TABLE USUARIOS CHANGE contrasena password  varchar(200) NOT NULL;

-- ------------------------------------------------------------------------------------- --
-- 09. Eliminar Columna. --------------------------------------------------------------- --
--     ALTER TABLE __ DROP __ : -------------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
ALTER TABLE CATEGORIAS DROP otras;

-- ------------------------------------------------------------------------------------- --
-- 10. Agregar Valor x Defecto Columna. ------------------------------------------------ --
--     ALTER TABLE __ ALTER __ SET DEFAULT __ :	---------------------------------------- --
-- ------------------------------------------------------------------------------------- --
ALTER TABLE USUARIOS ALTER id_usuario SET DEFAULT 1;

-- ------------------------------------------------------------------------------------- --
-- 11. Eliminar Valor x Defecto Columna. ----------------------------------------------- --
--     ALTER TABLE __ ALTER __ DROP DEFAULT : ------------------------------------------ --
-- ------------------------------------------------------------------------------------- --
ALTER TABLE USUARIOS ALTER id_usuario DROP DEFAULT;

-- ------------------------------------------------------------------------------------- --
-- 12. Mostrar Creación Tabla. --------------------------------------------------------- --
--     SHOW CREATE TABLE __ : ---------------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
SHOW CREATE TABLE USUARIOS;
SHOW CREATE TABLE PEDIDOS;
SHOW CREATE TABLE PAGOS;

-- ------------------------------------------------------------------------------------- --
-- 13. Eliminar Restricción. ----------------------------------------------------------- --
--     ALTER TABLE __ DROP CONSTRAINT __ : --------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
ALTER TABLE PEDIDOS DROP FOREIGN KEY fk_pedidos_formulas;
-- ------------------------------------------------------------------------------------- --
-- 14. Eliminar Índice. ---------------------------------------------------------------- --
--     ALTER TABLE __ DROP INDEX __ : -------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
ALTER TABLE PEDIDOS DROP INDEX ind_pedidos_formulas;
--------------------------------------------------------------------------------------- --
-- 15. Eliminar Llave Primaria. -------------------------------------------------------- --
--     ALTER TABLE __ DROP PRIMARY KEY : ----------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
ALTER TABLE ROLES DROP PRIMARY KEY;
ALTER TABLE PAGOS DROP PRIMARY KEY;

-- ------------------------------------------------------------------------------------- --
-- 16. Limpiar Registros. -------------------------------------------------------------- --
--     TRUNCATE __ : ------------------------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
TRUNCATE PRODUCTOS;

-- ------------------------------------------------------------------------------------- --
-- 17. Eliminar Tabla. ----------------------------------------------------------------- --
--     DROP TABLE __ : ----------------------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
DROP TABLE CATEGORIAS;

-- ------------------------------------------------------------------------------------- --
-- 18. Crear Tabla. -------------------------------------------------------------------- --
--     CREATE TABLE __ ( __ , __ ) : --------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
CREATE TABLE ROLES (
  id_rol INT NOT NULL AUTO_INCREMENT,
  nombre VARCHAR(45) NOT NULL);

-- ------------------------------------------------------------------------------------- --
-- 19. Renombrar Tabla. ---------------------------------------------------------------- --
--     RENAME TABLE __ TO __ : --------------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
RENAME TABLE USUARIOS TO PERSONAS;
RENAME TABLE PAGOS TO TRANSFERENCIAS;

-- ------------------------------------------------------------------------------------- --
-- 20. Crear Llave Primaria. ----------------------------------------------------------- --
--     ALTER TABLE __ ADD PRIMARY KEY ( __ ) : ----------------------------------------- --
-- ------------------------------------------------------------------------------------- --
ALTER TABLE ROLES ADD PRIMARY KEY (id_rol);

-- ------------------------------------------------------------------------------------- --
-- 21. Crear Índice Campo. ------------------------------------------------------------- --
--     CREATE INDEX __ ON __ ( __ ) :  ------------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
CREATE INDEX ind_pedidos_formulas ON FORMULAS (id_formula);
-- ------------------------------------------------------------------------------------- --
-- 22. Crear Índice Multicampo. -------------------------------------------------------- --
--     CREATE INDEX _ ON _ ( __ , __ ) : ----------------------------------------------- -- 
-- ------------------------------------------------------------------------------------- --
CREATE INDEX ind_pedidos_productos
ON PEDIDOS_PRODUCTOS (id_pedido, id_producto);

-- ------------------------------------------------------------------------------------- --
-- 23. Crear Índice Único. ------------------------------------------------------------- --
--     CREATE UNIQUE INDEX __ ON __ ( __ ) : ------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
CREATE UNIQUE INDEX ind_pedidos_formulas ON FORMULAS (id_formula);
-- ------------------------------------------------------------------------------------- --
-- 24. Crear Restricción. -------------------------------------------------------------- --
--     ALTER TABLE __ ADD CONSTRAINT __ FOREIGN KEY ( __ ) REFERENCES __ ( __ ) -------- --
--     ON DELETE CASCADE ON UPDATE CASCADE : ------------------------------------------- --
-- ------------------------------------------------------------------------------------- --
ALTER TABLE PAGOS ADD 
CONSTRAINT fk_pagos_pedidos 
	FOREIGN KEY (id_pedido)
	REFERENCES PEDIDOS (id_pedidos)
	ON DELETE CASCADE
	ON UPDATE CASCADE;
